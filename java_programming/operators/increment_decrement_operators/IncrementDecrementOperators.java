class IncrementDecrementOperators 
{
	public static void main(String[] args) 
	{
		//increment examples
		int a=10;
		System.out.println(a);
		//post increment
		System.out.println(a++);
		System.out.println(a);
		//pre increment
		System.out.println(++a);
		
		
		System.out.println("\n");

		//Decrement
		//predecrement
		System.out.println(--a);
		//postdecrement
		System.out.println(a--);
		System.out.println(a);
	}
}
