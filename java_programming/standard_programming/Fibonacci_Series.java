import java.util.Scanner;
class Fibonacci_Series
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		int a=0;
		int b=1;
		int end=sc.nextInt();
		int c=0;
		System.out.println("fiboancci series");
		for(int i=1;c<=end;i++)
		{	
			System.out.print(c+", ");
			a=b;
			b=c;
			c=a+b;
		}
		System.out.println();
		System.out.println("fiboancci series in reverse order........");
		for(int i=end;a>=0;i--)
		{
			System.out.print(b+",");
			c=b;
			b=a;
			a=c-b;
		}
		System.out.println();
	}
}
